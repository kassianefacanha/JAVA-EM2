public class Carro {

   private String nome,cor,modelo,nome_dono;
    private int ano,n_rodas,n_portas,capacidade;

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public int getCapacidade() {
        return capacidade;
    }

    public void setCapacidade(int capacidade) {
        this.capacidade = capacidade;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getN_portas() {
        return n_portas;
    }

    public void setN_portas(int n_portas) {
        this.n_portas = n_portas;
    }

    public int getN_rodas() {
        return n_rodas;
    }

    public void setN_rodas(int n_rodas) {
        this.n_rodas = n_rodas;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNome_dono() {
        return nome_dono;
    }

    public void setNome_dono(String nome_dono) {
        this.nome_dono = nome_dono;
    }


}
